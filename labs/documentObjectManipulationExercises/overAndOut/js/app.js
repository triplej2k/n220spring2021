let dvSquare = document.getElementById("dvSquare");

dvSquare.style.width = "100px";
dvSquare.style.height = "100px";
dvSquare.style.backgroundColor = "#4eb8f5";

function mouseOver() {
    dvSquare.style.width = "100px";
    dvSquare.style.height = "100px";
    dvSquare.style.backgroundColor = "#000000";
}

function mouseOut() {
    dvSquare.style.width = "100px";
    dvSquare.style.height = "100px";
    dvSquare.style.backgroundColor = "#4eb8f5";
}
